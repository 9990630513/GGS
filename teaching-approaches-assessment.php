                                <b>Teaching approach and assessment in IBDP</b>
                                <p>
                                  The IB diploma programme is different and distinct simply because of the pedagogical principles which
                                  guide it. The approach of interdisciplinary and inquiry-based learning is the foundation and the 
                                  framework for this student-centred experiential learning curriculum. Student’s natural thirst for 
                                  knowledge is fostered from the very beginning and is steered towards essential learning contents. 
                                  Students learn how to learn and how to make connections between the building blocks of knowledge 
                                  known as areas of knowledge in IB. The teaching never stops at ‘what’; it moves on to ‘how’  ‘why’ and 
                                  even encourages asking ‘can it be different?’ The challenge lies in redefining the art of learning, in truly 
                                  expanding the mind and become learners for life. Approaches to teaching and learning (ATL) within the 
                                  DP are deliberate strategies, skills and attitudes that permeate the teaching and learning environment. 
                                  In the DP students develop skills from the five ATL categories: thinking, research, social, self-
                                  management and communication.
                                <p/>
                                <p>
                                  DP assessments are designed to emphasize the development of higher order thinking skills. They are
                                  criterion related and focus on students’ ability to think analytically and critically, to integrate and apply 
                                  their learning, to work collaboratively and to communicate what they have learned in writing and orally.
                                </p>
